function onetext(){
	var one = document.getElementById("one");
	var one1 = window.getComputedStyle(one);
	if (one1.color == 'rgb(255, 255, 255)'){
		one.style.color = 'rgb(0, 0, 0)';
	} 
	document.getElementById("one").innerHTML="Раз"
}

